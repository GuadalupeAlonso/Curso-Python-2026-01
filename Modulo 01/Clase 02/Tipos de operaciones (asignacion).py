#Asignacion
c = 5
c += 2  # c = c + 2
print("Asignacion suma: ", c)
c -= 1  # c = c - 1
print("Asignacion resta: ", c)
c *= 3  # c = c * 3
print("Asignacion multiplicacion: ", c)
c /= 2  # c = c / 2
print("Asignacion division: ", c)
c //= 2  # c = c // 2
print("Asignacion division entera: ", c)
c %= 3  # c = c % 3
print("Asignacion modulo: ", c)
a= int(input("Dame un numero: "))
