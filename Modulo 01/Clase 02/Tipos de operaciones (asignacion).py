print(f"La asignacion resta de a -= 2 es: ", a - 2)
print(f"La asignacion multiplicacion de a *= 3 es: {a * 3}")
print(f"La asignacion division de a /= 2 es: {a / 2}")
print(f"La asignacion division entera de a //= 2 es: {a // 2}")
print(f"La asignacion modulo de a %= 3 es: {a % 3}")