#Comparacion
x = 10
y = 20
print("Igualdad: ", x == y)
print("Desigualdad: ", x != y)
print("Mayor que: ", x > y)
print("Menor que: ", x < y)
#Logicos
p = True
q = False
print("AND: ", p and q)
print("OR: ", p or q)
print("NOT: ", not p)
a= int(input("Dame un numero: "))
b= int(input("Dame otro numero: "))
print(f"La igualdad de {a} == {b} es: ", a == b)
print(f"La desigualdad de {a} != {b} es: ", a != b)
print(f"El mayor que {a} > {b} es: ", a > b)
print(f"El menor que {a} < {b} es: ", a < b)
print(f"El AND de {a} > 5 and {b} < 10 es: ", a > 5 and b < 10)
print(f"El OR de {a} > 5 or {b} < 10 es: ", a > 5 or b < 10)
print(f"El NOT de not {a} > 5 es: ", not a > 5)