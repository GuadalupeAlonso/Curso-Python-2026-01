#caracteristicas de las listas
#las listas son mutables, es decir, se pueden modificar sus elementos
mi_lista = [1, 2, 3]
print("Lista original:", mi_lista)
mi_lista[0] = 10
print("Lista modificada:", mi_lista)
#las listas mantienen el orden de los elementos
mi_lista = ["a", "b", "c"]
print("Lista ordenada:", mi_lista)
#las listas permiten elementos duplicados
mi_lista = [1, 2, 2, 3, 3, 3]
print("Lista con duplicados:", mi_lista)
